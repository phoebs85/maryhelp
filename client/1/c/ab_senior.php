                <p>我們誠摯邀請各位校友為母校的高中學生擔任學長，把你們的專業知識、人生經驗和強項傳授給在學的師妹。這些分享對成長中的年青人是非常重要的。無論您是哪一個年代的校友,祇要撥出一點時間和精力,就可幫助成長中的師妹,在這重要的人生階段與她們並肩同行。如欲索取更多資料，請電郵至<a href="mailto:Alumnae-in-Action@olc.edu.hk" target="_blank">Alumnae-in-Action@olc.edu.hk</a> 與學長組組長Esther Chak 翟鳳媚、Cecilia Cheon 張偉麗，或與聖母書院升學及職業輔導組主任 Mandy Ho 何燕玉老師聯絡。</p>


